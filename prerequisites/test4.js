// In the code snippet below, what will be logged to the console?

// In javascript foo++ and ++foo have different behaviors. As you can see, ++foo increments foo and then returns
// the value to you. Whereas foo++ returns the current value of foo to you, and then increments it later.

let foo = 3;
console.log(foo);
console.log(++foo);
console.log(foo++);
console.log(foo);

// 3
// 4
// 4
// 5

